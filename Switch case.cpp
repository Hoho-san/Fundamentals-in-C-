#include <iostream>
using namespace std;

int main() {
  char day = 'a';
  switch (day) {
  case 'a':
    cout << "Monday";
    break;
  case 'b':
    cout << "Tuesday";
    break;
  case 'c':
    cout << "Wednesday";
    break;
  case 'd':
    cout << "Thursday";
    break;
  case 'e':
    cout << "Friday";
    break;
  case 'f':
    cout << "Saturday";
   break;
  case 'g':
    cout << "Sunday";
    break;
  }
  return 0;
}

